const getIconColor = color => {
  switch (color) {
    case "greenLight":
      return "green"

    case "green":
      return "green"

    case "blueLight":
      return "blue"

    case "blue":
      return "blue"

    case "pinkLight":
      return "pink"

    case "pink":
      return "pink"

    case "apricotLight":
      return "apricot"

    case "apricot":
      return "apricot"

    case "primary":
      return "primary"

    default:
      return "blue"
  }
}

export default getIconColor
